package classes;

public class InfoColisao {

	private int aviaoA;
	private int aviaoB;
	private double tempoColisao;
	private double diferencaTempo;
	public int getAviaoA() {
		return aviaoA;
	}
	public void setAviaoA(int aviaoA) {
		this.aviaoA = aviaoA;
	}
	public int getAviaoB() {
		return aviaoB;
	}
	public void setAviaoB(int aviaoB) {
		this.aviaoB = aviaoB;
	}
	public double getTempoColisao() {
		return tempoColisao;
	}
	public void setTempoColisao(double tempoColisao) {
		this.tempoColisao = tempoColisao;
	}
	public double getDiferencaTempo() {
		return diferencaTempo;
	}
	public void setDiferencaTempo(double diferencaTempo) {
		this.diferencaTempo = diferencaTempo;
	}
	
	
}
